function filterAndPreview() {
  const key = searchBox.value.toLowerCase().normalize("NFC");
  selectedKeyword = key;
  const msgBox = document.getElementById("message");
  msgBox.innerHTML = "";
  previewBox.innerHTML = "";
  quizBox.innerHTML = "";
  resultBox.innerHTML = "";
  submitBtn.style.display = "inline-block";
  nextBtn.style.display = "none";
  

  if (!key) return;

  filteredQuestions = allQuestions.filter(q =>
   
                                         
    q.question_en.toLowerCase().includes(key) ||
    q.question_hi.toLowerCase().normalize("NFC").includes(key)
);

  if (filteredQuestions.length === 0) {
    msgBox.innerHTML = `<div class="notice">❌ No questions found for this keyword.</div>`;
    return;
  }

  msgBox.innerHTML = `<div class="notice">👉 <strong>${filteredQuestions.length} questions found for this keyword.⬇️ enter your name to start the mock test.</strong></div>`;

  

  const subset = filteredQuestions;
  subset.forEach((q, idx) => {
    const box = document.createElement("div");
    box.className = "question-box";
    box.innerHTML = `<b>${idx + 1}. (${q.examDate} | Q.No ${q.questionNumber})</b><br>
      ${selectedLang === 'hi' ? q.question_hi : selectedLang === 'en' ? q.question_en : q.question_hi + '<br>' + q.question_en}`;
    if (q.image) box.innerHTML += `<br><img src="${q.image}" style="max-width:100%; margin:10px 0;">`;

    const opts = selectedLang === 'hi' ? q.options_hi : selectedLang === 'en' ? q.options_en : q.options_hi.map((h, i) => getCombinedOption(h, q.options_en[i]));
    opts.forEach((opt, i) => {
      const optDiv = document.createElement("div");
      optDiv.className = "option";
      optDiv.innerHTML = `<label><input type="radio" name="q${idx}" value="${i}"> ${opt}</label>`;
      const radio = optDiv.querySelector("input");
      radio.addEventListener("change", () => { userAnswers[idx] = i; });
      box.appendChild(optDiv);
    });
    quizBox.appendChild(box);
  });
  submitBtn.style.display = "inline-block";
}


 
  
