function getCombinedOption(hi, en) {
  const getCore = str => str
    .replace(/^[0-9]+[.)-]?\s*/, '')    // "1. ", "2) ", "3- " आदि हटाओ
    .replace(/[()]/g, '')              // कोष्ठक हटाओ
    .replace(/english/gi, '')          // "english" शब्द हटाओ
    .trim()
    .toLowerCase();

  const hiCore = getCore(hi);
  const enCore = getCore(en);

  const matchInBracket = hi.match(/(.*?)/);
  if (matchInBracket && getCore(matchInBracket[1]) === enCore) {
    return hi.trim(); // ✅ Hindi में ही English है
  }

  if (hiCore === enCore) {
    return hi.trim(); // ✅ दोनों एक जैसे हैं
  }

  // ✅ सिर्फ तभी जोड़ो जब ऊपर से return नहीं हुआ
  const enWithoutNumber = en.replace(/^[0-9]+[.)-]?\s*/, '').trim();
  return `${hi.trim()} / ${enWithoutNumber}`;
}

